package Methods;

public class Calculator {
	
	float addition(int num1, int num2)
	{
		int a = num1+num2;
		return a;
	}
	
	float subtraction(int num1, int num2)
	{
		int a = num1-num2;
		return a;
	}
	
	float multiplication(int num1, int num2)
	{
		int a = num1*num2;
		return a;
	}
	
	float division(int num1, int num2)
	{
		int a = num1/num2;
		return a;
	}

}
