package OOPs;

public class AdditionDemo {
	
	int num1=5;
	int num2=3;
	int result;
	
	void addition()
	{
		result = num1+num2;
		System.out.println(result);
	}
	
	void subtraction()
	{
		result = num1-num2;
		System.out.println(result);
	}
	
	void multiplication()
	{
		result = num1*num2;
		System.out.println(result);
	}
	
	void division()
	{
		result = num1/num2;
		System.out.println(result);
	}

}
