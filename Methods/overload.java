package Methods;

public class overload {
	
	void test(int a)
	{
		System.out.println(a);
	}
	
	void test(int a, int b)
	{
		System.out.println(a);
		System.out.println(b);
	}
	
	void test(int a, int b, int c)
	{
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
	}
	
	void test(int a, int b, int c, int d)
	{
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
	}

}
