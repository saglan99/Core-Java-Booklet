package Arrays;

public class Chocolate {
	
	public int sn;
	public String name;
	public int price;
	
	public Chocolate(int sn, String name, int price) {
		super();
		this.sn = sn;
		this.name = name;
		this.price = price;
	}

	@Override
	public String toString() {
		return "Chocolate [sn=" + sn + ", name=" + name + ", price=" + price + "]";
	}
	
	
	
	

}
