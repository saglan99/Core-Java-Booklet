package CollectionSorting;

public class cw2 {
	
	int id;
	String name;
	int salary;
	
	public cw2(int id, String name, int salary) {
		this.id = id;
		this.name = name;
		this.salary = salary;
	}
	
	@Override
	public String toString() {
		
		return id+" "+name+" "+salary;
		
	}
	

}
