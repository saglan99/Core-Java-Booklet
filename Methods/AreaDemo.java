package Methods;

public class AreaDemo {
	
	void area(int a)
	{
		int area = a*a;
		System.out.println("area of square is :"+area);
	}
	
	void area(int a, int b)
	{
		int area = a*b;
		System.out.println("area of square is :"+area);
	}
	
	void area(int a, int b , int c)
	{
		int area = a*b*c;
		System.out.println("area of square is :"+area);
	}

}
