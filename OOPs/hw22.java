package OOPs;

public class hw22 {
	
	int add(int a, int b)
	{
		int c = a+b; 
		return c;
	}
	
	int sub(int a, int b)
	{
		int c = a-b; 
		return c;
	}
	
	int mul(int a, int b)
	{
		int c = a*b;
		return c;
	}
	
	int div(int a, int b)
	{
		int c = a/b;
		return c;
	}

}
