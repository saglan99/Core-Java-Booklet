package BasicsOfJava;

public class hw3 {
	
	void circle()
	{
		float r=4, pi=3.14f, area, perimeter;
		
		
		area = pi * r * r;
		
		perimeter = 2 * pi * r;
		
		System.out.println("area of circle is :" +area);
		
		System.out.println("perimeter of circle is :" +perimeter);
	}

}
